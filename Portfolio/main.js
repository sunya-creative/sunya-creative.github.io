// 9. Interactive Hero: bg_c.png Rotation respond to mouse
function initHeroInteractions() {
    const heroSection = document.getElementById("hero");
    const introContainer = document.getElementById("intro-container");
    const rotatingCircle = document.getElementById("hero-rotating-circle");

    if (!introContainer || !rotatingCircle) return;

    if (heroSection) {
        heroSection.addEventListener("click", () => {
            const aboutSection = document.getElementById("about");
            if (aboutSection) {
                aboutSection.scrollIntoView({ behavior: "smooth" });
            }
        });
    }

    // Rotate speed and angle variables for rotatingCircle
    let targetAngle = 0;
    let currentAngle = 0;

    // Mouse movement inside Hero Container
    introContainer.addEventListener("mousemove", (e) => {
        const rect = introContainer.getBoundingClientRect();

        // Rotate circle background based on mouse position relative to center
        const centerX = rect.left + rect.width / 2;
        const centerY = rect.top + rect.height / 2;

        // Calculate angle between center and mouse
        const dx = e.clientX - centerX;
        const dy = e.clientY - centerY;

        // Angle in degrees
        targetAngle = Math.atan2(dy, dx) * (180 / Math.PI);
    });

    // Smooth angle interpolation for rotatingCircle
    function animateCircleRotation() {
        if (rotatingCircle) {
            // Lerp angle
            let diff = targetAngle - currentAngle;

            // Normalize diff to -180 to 180 to prevent wrapping spin glitches
            diff = Math.atan2(Math.sin(diff * Math.PI / 180), Math.cos(diff * Math.PI / 180)) * (180 / Math.PI);

            currentAngle += diff * 0.08; // Smooth factor
            rotatingCircle.style.transform = `rotate(${currentAngle}deg)`;
        }
        requestAnimationFrame(animateCircleRotation);
    }
    animateCircleRotation();
}
initHeroInteractions();

// 10. Smart Scroll Header: Keep header fixed, apply color toggle below hero section
function initScrollHeader() {
    const headerEl = document.querySelector(".header");
    const heroSection = document.getElementById("hero");

    if (!headerEl) return;

    window.addEventListener("scroll", () => {
        const currentScrollY = window.scrollY;
        const heroHeight = heroSection ? heroSection.offsetHeight : window.innerHeight;

        // Scrolled-in State: Apply blur filter and change font colors to black below hero
        if (currentScrollY > heroHeight - 80) {
            headerEl.classList.add("header--scrolled");
        } else {
            headerEl.classList.remove("header--scrolled");
        }
    }, { passive: true });
}
