// 2b. Dynamic Noise Background Generator
function initNoiseBackground() {
    const canvas = document.createElement('canvas');
    canvas.width = 128;
    canvas.height = 128;
    const ctx = canvas.getContext('2d');
    const imgData = ctx.createImageData(canvas.width, canvas.height);
    const data = imgData.data;
    for (let i = 0; i < data.length; i += 4) {
        const val = Math.floor(Math.random() * 255);
        data[i] = val;
        data[i + 1] = val;
        data[i + 2] = val;
        data[i + 3] = 14; // Subtle grain opacity (~5.5%)
    }
    ctx.putImageData(imgData, 0, 0);
    const noiseUrl = canvas.toDataURL();
    document.documentElement.style.setProperty('--noise-pattern', `url(${noiseUrl})`);
}
initNoiseBackground();

// 3. Custom Magnet Mouse Cursor
const cursor = document.getElementById("custom-cursor");
const cursorDot = cursor.querySelector(".cursor-dot");
const cursorCircle = cursor.querySelector(".cursor-circle");

const solidIntroText = document.querySelector(".intro-text--solid");
let mouseX = 0;
let mouseY = 0;
let cursorX = 0;
let cursorY = 0;
const speed = 0.16; // Smooth interpolation speed

window.addEventListener("mousemove", (e) => {
    mouseX = e.clientX;
    mouseY = e.clientY;
});

window.addEventListener("touchmove", (e) => {
    if (e.touches && e.touches.length > 0) {
        mouseX = e.touches[0].clientX;
        mouseY = e.touches[0].clientY;
    }
}, { passive: true });

window.addEventListener("touchstart", (e) => {
    if (e.touches && e.touches.length > 0) {
        mouseX = e.touches[0].clientX;
        mouseY = e.touches[0].clientY;
        cursor.classList.add("hover-intro");
    }
}, { passive: true });

window.addEventListener("touchend", () => {
    cursor.classList.remove("hover-intro");
}, { passive: true });

function animateCursor() {
    cursorX += (mouseX - cursorX) * speed;
    cursorY += (mouseY - cursorY) * speed;

    cursorDot.style.left = `${mouseX}px`;
    cursorDot.style.top = `${mouseY}px`;

    cursorCircle.style.left = `${cursorX}px`;
    cursorCircle.style.top = `${cursorY}px`;

    // Update clip-path for solid text reveal relative to the container
    if (solidIntroText) {
        const rect = solidIntroText.getBoundingClientRect();
        const relativeX = cursorX - rect.left;
        const relativeY = cursorY - rect.top;
        const radius = cursor.classList.contains("hover-intro") ? 110 : 0;
        solidIntroText.style.clipPath = `circle(${radius}px at ${relativeX}px ${relativeY}px)`;
    }

    requestAnimationFrame(animateCursor);
}
animateCursor();

