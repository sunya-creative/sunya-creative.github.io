    // Scale for high DPI displays
    function resizeRadar() {
        const rect = radarCanvas.getBoundingClientRect();
        const dpr = window.devicePixelRatio || 1;
        width = rect.width;
        height = rect.height;
        radarCanvas.width = width * dpr;
        radarCanvas.height = height * dpr;
        ctx.scale(dpr, dpr);
    }

    window.addEventListener("resize", resizeRadar);
    resizeRadar();

    // 3D Projection math
    function rotate3D(point, pitch, yaw, roll) {
        let x = point.x, y = point.y, z = point.z;

        // Rotate X (pitch)
        let cosX = Math.cos(pitch), sinX = Math.sin(pitch);
        let y1 = y * cosX - z * sinX;
        let z1 = y * sinX + z * cosX;

        // Rotate Y (yaw)
        let cosY = Math.cos(yaw), sinY = Math.sin(yaw);
        let x2 = x * cosY + z1 * sinY;
        let z2 = -x * sinY + z1 * cosY;

        // Rotate Z (roll)
        let cosZ = Math.cos(roll), sinZ = Math.sin(roll);
        let x3 = x2 * cosZ - y1 * sinZ;
        let y3 = x2 * sinZ + y1 * cosZ;

        return { x: x3, y: y3, z: z2 };
    }

    // 1. VMD Cube Vertices (Space/Installation display)
    const cubeVertices = [];
    const size = 36;
    for (let x of [-1, 1]) {
        for (let y of [-1, 1]) {
            for (let z of [-1, 1]) {
                cubeVertices.push({ x: x * size, y: y * size, z: z * size });
            }
        }
    }
    const cubeEdges = [
        [0, 1], [1, 3], [3, 2], [2, 0], // front
        [4, 5], [5, 7], [7, 6], [6, 4], // back
        [0, 4], [1, 5], [2, 6], [3, 7]  // pillars
    ];

    // 2. Product Cylinder Vertices (Container/Packaging design)
    const cylVertices = [];
    const segments = 12;
    const cylHeight = 72;
    const radius = 28;
    // Bottom circle
    for (let i = 0; i < segments; i++) {
        let angle = (i / segments) * Math.PI * 2;
        cylVertices.push({ x: Math.cos(angle) * radius, y: -cylHeight / 2, z: Math.sin(angle) * radius });
    }
    // Top circle
    for (let i = 0; i < segments; i++) {
        let angle = (i / segments) * Math.PI * 2;
        cylVertices.push({ x: Math.cos(angle) * radius, y: cylHeight / 2, z: Math.sin(angle) * radius });
    }
    const cylEdges = [];
    for (let i = 0; i < segments; i++) {
        cylEdges.push([i, (i + 1) % segments]);
        cylEdges.push([i + segments, ((i + 1) % segments) + segments]);
        cylEdges.push([i, i + segments]); // pillars
    }

